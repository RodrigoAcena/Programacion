public class Metodos03 {
	public static boolean esDigito(char letra) {
		if(letra == '1') {
			return true;
		} else if(letra == '2') {
			return true;
		} else if(letra == '3') {
			return true;
		} else if(letra == '4') {
			return true;
		} else if(letra == '5') {
			return true;
		} else if(letra == '6') {
			return true;
		} else if(letra == '7') {
			return true;
		} else if(letra == '8') {
			return true;
		} else if(letra == '9') {
			return true;
		} else if(letra == '0') {
			return true;
		} else {
			return false;
		}
		
	}
}
